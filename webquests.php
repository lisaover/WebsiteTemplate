            <H3>Pythagorean Theorem Life Application Webquest</H3>
            <p>Welcome to the Life Application webquest for the Pythagorean Theorem. Your mission has three phases:</p>
<ul>
<li>Phase 1: Consider a real-life scenario. Break down the problem by drawing a diagram to show known and unknown information and by researching possibilities to satisfy the criterion.</li>
<li>Phase 2: Create a table to organize your information. Synthesize and evaluate all possible solutions against given criterion and propose a solution.</li>
<li>Phase 3: Create your own problem that requires the Pythagorean Theorem. Include at least one criterion that requires gathering information to compare and contrast two or more options. Solve your problem.</li>
</ul>
<p class="italic"><em><strong>Objectives</strong></em></p>
<p>After completing the Life Application webquest, students will be able to...</p>
<ul>
<li>Correctly analyze a given problem listing known information and unknown information.</li>
<li>Correctly gather information required to solve the problem given the Internet.</li>
<li>Correctly identify what the problem is asking them to find.</li>
<li>Correctly construct an expression using variables and numeric values to represent the value they need to find.</li>
<li>Correctly calculate the value they need to find.</li>
<li>Correctly organize their calculated and gathered information into a table.</li>
<li>Adequately propose a solution to the problem given the information in their table.</li>
<li>Adequately create, develop a plan for solving, and solve an original problem.</li>
</ul>
<p><em><strong>Life Application</strong></em><br /><em><strong>Phase 1: The Problem</strong></em></p>
<p>Your neighbor offers to pay you to complete a landscaping project for him. The money he offered will cover your final payment for your senior trip to Florida or the new gaming system you’ve been wanting. The project involves researching the sizes of trees that he likes and selecting and planting one that best meets his criterion.</p>
<p>The criterion is that the tree must not block his view of the mountains when it is full grown. The neighbor must be able to see over the top of the tree when he sits on his back patio. The distance from the ground to his eyes when he is seated is 4 feet. The distance from his patio to the end of his yard, where he wants to plant the tree, is 100 feet. The distance from his gaze, which is about 30 degrees above the horizontal, to the tops of the mountains is 115 feet.</p>
