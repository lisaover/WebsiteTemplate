            <H3>Scientific Notation Webquest</H3>
            <p>This webquest is a fun way for students to recall scientific notation and its purpose. After a lesson to help students recall how to convert numbers to and from scientific notation, students logged on to their school accounts and found two facts each containing a number that was either less than 0.001 or greater than 1000. Students then wrote the number in scientific or standard notation or both if the original number was in neither form.</p>
<p class="note italic"><em><strong>Reflection</strong></em></p>
<p>Some students were really involved in this activity and obviously had fun finding their facts. They found some really interesting ones such as the estimated number of blades of grass on the Earth, the number of gallons of water on the Earth, the radius of an electron, the number of individual M&amp;Ms made per day, the number of people employed by McDonald's, etc. My students were very competitive and started competing for the largest and smallest numbers.</p>
