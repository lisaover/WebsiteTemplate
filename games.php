            <H3>Math Concept Clues Review Game</H3>
            
            <p>Math Concept Clues is a review game that focuses on math terminology, concepts, formulas, and procedures. The objectives of Math Concept Clues are to learn a new game and follow the rules, to review important math skills and content, and to learn good sportsmanship.</p>
            <h4>Description</h4>
            <p>Students will be assigned to one of two teams. As a group, members from each team will decide on a team name. Each team will take turns selecting a clue-giver who has one minute to describe a math concept and get his or her teammates to name it correctly. For example, for the concept “Midpoint,” the clue-giver may give the following clue: “A point that divides a line segment in half.” If anyone says “midpoint” before the time runs out, the clue-giver’s team gets a point. Members from the opposing team must pay attention quietly, because they will have a chance to name the concept and to earn a point if the clue-giver’s team fails to name it correctly in the allotted time.</p>
            <h4>Objectives</h4>
            <p>The objectives of Math Concept Clues are to learn a new game and follow the rules, to review important math skills and content, and to learn good sportsmanship.</p>
            <h4>Materials</h4>
            <ul>
            <li>Large index cards</li>
            <li>Timer</li>
            <li>Buzzer</li>
            </ul>
            <h4>Clue Cards</h4>
            <p>On each index card, I will write a math term or the name of a concept, formula, or procedure that I want the class to review. I will sketch or paste an illustration of the concept to help students remember what it is. I will also add “Extra Help” clues on the back, which will include key words or phrases that will help students who are struggling with describing their concept.</p>
            <h4>Clue-giver and Moderators</h4>
            <p>The clue-giver will sit at a desk or table at the front of the room. I will construct a barrier so other students cannot see the clue card. Two moderators from the opposing team will sit on either side of the clue-giver. Their jobs are to keep track of the time, make sure the clue-giver does not say the actual name of the concept, and tally the points.</p>
            <p>I will select a new clue-giver and two new moderators at random for each round. I will alternate between the two teams. Because there are two moderators for each round, students will moderate twice as many times as they will give clues.</p>
            <h4>Rules</h4>
            <ol>
            <li>All students must treat teammates and members of the opposing team with respect and kindness. If a student is mean, disrespectful, or obnoxious toward anyone, that student’s team will receive a foul and lose a turn.</li>
            <li>If the clue-giver’s team correctly names the concept in the allotted time, the clue-giver’s team earns a point.</li>
            <li>If the clue-giver does not use the “Extra Help” part of the card, his or her team earns an additional point if they name the concept correctly before the time runs out.</li>
            <li>If the clue-giver says the name of the concept, the moderator will sound the buzzer. The opposing team then has a chance to name the concept for a point.</li>
            <li>If the clue-giver’s team fails to name the concept before the time runs out, the opposing team has a chance to name it for a point.</li>
            <li>If a member from the opposing team shouts out the answer before time runs out for the clue-giver’s team, the point is awarded to the clue-giver’s team.</li>
            </ol>
            <h4>Variation</h4>
            <p>Another way to play this game is to create Clue Cards that have the concept name and description without any illustration. Instead of describing their concept in words to their teammates, students draw a picture on the board to represent their concept. Students can label the picture but must omit the name of the concept. Students cannot give verbal clues in this version. Teammates have one minute to guess the name of the concept based on the drawing.</p>
