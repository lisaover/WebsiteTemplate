            <H3>Types of Lines Graphic Organizer</H3>
            <p>During my student teaching, one student who was an English language learner struggled with the different types of lines and their respective equations. To help him process and remember the similarities and differences of parallel, perpendicular, and coincident lines, I created a graphic organizer (after an example in the textbook) where students could model these three concepts verbally, visually, and with formulas. Not everyone took advantage of this, however, the ELL student did fill it out and proceeded to get every related question on the test correct.</p>
